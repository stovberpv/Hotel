<?php
    #---------------------------------------------------------------------------------
    # debug settings
    #---------------------------------------------------------------------------------
    if (true) {
        ini_set('display_errors', 1);
        ini_set('display_startup_errors', 1);
        error_reporting(E_ALL);
    }
    #---------------------------------------------------------------------------------
    # 
    #---------------------------------------------------------------------------------
    require $_SERVER['DOCUMENT_ROOT'] . '/db/utils.php';
    #---------------------------------------------------------------------------------
    # check autorization
    #---------------------------------------------------------------------------------
    !isAuthorized() && die(err2echo(23, '', ''));
    #
    $user = getAuthUserName();
    #---------------------------------------------------------------------------------
    # connect to db
    #---------------------------------------------------------------------------------
    require $_SERVER['DOCUMENT_ROOT'] . '/db/conn.php';
    #---------------------------------------------------------------------------------
    # 
    #---------------------------------------------------------------------------------
    isset($_GET["year"])  ? $year  = $_GET["year"]  : die(err2echo(24, 'Обновление Настройки.', ''));
    isset($_GET["month"]) ? $month = $_GET["month"] : die(err2echo(25, 'Обновление Настройки.', ''));
    #---------------------------------------------------------------------------------
    # 
    #---------------------------------------------------------------------------------
    $query = "UPDATE cf001 SET year = ?, month = ? WHERE user = ?";        
    !($stmt = $mysqli->prepare($query))               && die(err2echo(10, "Обновление Настройки. ", $mysqli));
    !($stmt->bind_param('sss', $year, $month, $user)) && die(err2echo(11, "Обновление Настройки. ", $mysqli));
    !($stmt->execute())                               && die(err2echo(12, "Обновление Настройки. ", $mysqli));   
    $stmt->close();
    #---------------------------------------------------------------------------------
    # 
    #---------------------------------------------------------------------------------
    $mysqli->close();
?>