<?php
    #---------------------------------------------------------------------------------
    # debug settings
    #---------------------------------------------------------------------------------
    if (true) {
        ini_set('display_errors', 1);
        ini_set('display_startup_errors', 1);
        error_reporting(E_ALL);
    }
    #---------------------------------------------------------------------------------
    # 
    #---------------------------------------------------------------------------------
    require $_SERVER['DOCUMENT_ROOT'] . '/db/utils.php';
    #---------------------------------------------------------------------------------
    # check autorization
    #---------------------------------------------------------------------------------
    if (!isAuthorized()) {
        die(err2echo(23,'', ''));
    }
    #---------------------------------------------------------------------------------
    # connect to db
    #---------------------------------------------------------------------------------
    require $_SERVER['DOCUMENT_ROOT'] . '/db/conn.php';
    #---------------------------------------------------------------------------------
    # 
    #---------------------------------------------------------------------------------
    $user = getAuthUserName();
    #---------------------------------------------------------------------------------
    # 
    #---------------------------------------------------------------------------------
    $query = "SELECT year, month FROM cf001 WHERE user = ?";        
    !($stmt = $mysqli->prepare($query))  && die(err2echo(10, "Выборка Настройки. ", $mysqli));
    !($stmt->bind_param('s', $user))     && die(err2echo(11, "Выборка Настройки. ", $mysqli));
    !($stmt->execute())                  && die(err2echo(12, "Выборка Настройки. ", $mysqli));   
    !($stmt->bind_result($year, $month)) && die(err2echo(13, "Выборка Настройки. ", $mysqli));
    !($stmt->fetch())                    && die(err2echo(14, "Выборка Настройки. ", $mysqli));
    #
    $data['year'] = $year;
    $data['month'] = $month;
    #
    $stmt->free_result();
    $stmt->close();
    #---------------------------------------------------------------------------------
    # 
    #---------------------------------------------------------------------------------
    $mysqli->close();
    #---------------------------------------------------------------------------------
    # result
    #---------------------------------------------------------------------------------
    $data['status'] = true;
    echo json_encode($data);
?>