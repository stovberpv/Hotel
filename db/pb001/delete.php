<?php
    #---------------------------------------------------------------------------------
    # debug settings
    #---------------------------------------------------------------------------------
    if (true) {
        ini_set('display_errors', 1);
        ini_set('display_startup_errors', 1);
        error_reporting(E_ALL);
    }
    #---------------------------------------------------------------------------------
    # 
    #---------------------------------------------------------------------------------
    require $_SERVER['DOCUMENT_ROOT'] . '/db/utils.php';
    #---------------------------------------------------------------------------------
    # check autorization
    #---------------------------------------------------------------------------------
    !isAuthorized() && die(err2echo(23, '', ''));
    #---------------------------------------------------------------------------------
    # connect to db
    #---------------------------------------------------------------------------------
    require $_SERVER['DOCUMENT_ROOT'] . '/db/conn.php';
    #---------------------------------------------------------------------------------
    # 
    #---------------------------------------------------------------------------------
    isset($_GET["name"])   ? $name   = $_GET["name"]   : $name   = "";
    isset($_GET["tel"])    ? $tel    = $_GET["tel"]    : $tel    = "";
    isset($_GET["city"])   ? $city   = $_GET["city"]   : $city   = "";
    #
    $tel = preg_replace('~\D+~', '', $tel);
    #---------------------------------------------------------------------------------
    # 
    #---------------------------------------------------------------------------------
    $qury = 'DELETE * FROM pb001 WHERE name = ? tel = ? city = ?';
    #
    !($stmt = $mysqli->prepare($query))             && die(err2echo(10, "Удаление контакта. ", $mysqli));
    !($stmt->bind_param('sis', $name, $tel, $city)) && die(err2echo(11, "Удаление контакта. ", $mysqli));
    !($stmt->execute())                             && die(err2echo(12, "Удаление контакта. ", $mysqli));  
    #
    $stmt->free_result();
    $stmt->close();
    #---------------------------------------------------------------------------------
    # 
    #---------------------------------------------------------------------------------
    $mysqli->close();
?>